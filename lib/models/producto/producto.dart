class Producto {
  int _id;
  String _nombre;
  int _precio;
  int _iva;

  int get id => _id;

  set id(int value) => _id = value;

  String get nombre => _nombre;

  set nombre(String value) => _nombre = value;

  int get precio => _precio;

  set precio(int value) => _precio = value;

  int get iva => _iva;

  set iva(int value) => _iva = value;
}
