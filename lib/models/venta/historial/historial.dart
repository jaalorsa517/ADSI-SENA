abstract class Historial {
  String _fechaPedido;
  String _nombreProducto;
  int _cantidad;

  // ignore: unnecessary_getters_setters
  String get fechaPedido => _fechaPedido;

  // ignore: unnecessary_getters_setters
  set fechaPedido(String value) => _fechaPedido = value;

  // ignore: unnecessary_getters_setters
  String get nombreProducto => _nombreProducto;

  // ignore: unnecessary_getters_setters
  set nombreProducto(String value) => _nombreProducto = value;

  // ignore: unnecessary_getters_setters
  int get cantidad => _cantidad;

  // ignore: unnecessary_getters_setters
  set cantidad(int value) => _cantidad = value;
}
